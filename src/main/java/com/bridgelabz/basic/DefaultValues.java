package com.bridgelabz.basic;

public class DefaultValues {
    public static void main(String[] args) {
        boolean x1 = false;
        String x2 = null;
        int x3 = 0;
        char x4 = 0;
        float x5 = 0;
        long x6 = 0;
        double x7 = 0;
        System.out.println("Default values are................");
        System.out.println("Boolean-" + x1);
        System.out.println("String-" + x2);
        System.out.println("Integer-" + x3);
        System.out.println("Character-" + x4);
        System.out.println("Float-" + x5);
        System.out.println("Long-" + x6);
        System.out.println("Double-" + x7);

    }
}
